    public void loadTable() {
        try {
            List<Student> lista = sr.findAll();
            stm.addList(lista);
            table.setModel(stm);
            stm.fireTableDataChanged();
        } catch (CrudFormException ex) {
            Logger.getLogger(StudentForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void tabelaSelectedIndexChange() {
        final ListSelectionModel rowSM = table.getSelectionModel();
        rowSM.addListSelectionListener(new ListSelectionListener() {

            @Override
            public void valueChanged(ListSelectionEvent e) {
                if (e.getValueIsAdjusting()) {
                    return;
                }

                ListSelectionModel rowSM = (ListSelectionModel) e.getSource();
                int selectedIndex = rowSM.getAnchorSelectionIndex();
                if (selectedIndex > -1) {
                    Student s = stm.getStudent(selectedIndex);
                    studentIdField.setText(s.getId().toString());
                    firstNameField.setText(s.getFirstName());
                    lastNameField.setText(s.getLastName());
                    facultyComboBox.setSelectedItem(s.getFacultyName());
                    facultyComboBox.repaint();

                }
            }
        });
    }
