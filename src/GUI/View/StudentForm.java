    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        try {
            int row = table.getSelectedRow();
            if(row == -1){
              Student s = new Student();
              
              s.setId(Integer.parseInt(studentIdField.getText())); 
              s.setFirstName(firstNameField.getText()); 
              s.setLastName(lastNameField.getText());
              s.setFacultyName((String)facultyComboBox.getSelectedItem());
          
              sr.create(s);
            }else{
                Student s = stm.getStudent(row);
               
                s.setId(Integer.parseInt(studentIdField.getText())); 
                s.setFirstName(firstNameField.getText());
                s.setLastName(lastNameField.getText());
                s.setFacultyName((String)facultyComboBox.getSelectedItem());
                sr.edit(s);
            }
            
            clear();
            loadTable();
        }catch(CrudFormException ex){
            JOptionPane.showMessageDialog(this, "E dhena ekziston!");
        }
    }//GEN-LAST:event_saveBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        int row = table.getSelectedRow();
        if (row != -1) {
            Object[] ob = {"Yes", "No"};
            int i = JOptionPane.showOptionDialog(this, "Are you sure you want to delete the selected field?", "Fshirja", JOptionPane.OK_OPTION, JOptionPane.QUESTION_MESSAGE, null, ob, ob[1]);
            if (i == 0) {
                Student s = stm.getStudent(row);
                try {
                    sr.delete(s);
                } catch (CrudFormException ex) {
                    JOptionPane.showMessageDialog(this, ex.getMessage());
                }
                clear();
                loadTable();
            } else {
                clear();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please select a field");
        }

    }//GEN-LAST:event_deleteBtnActionPerformed

